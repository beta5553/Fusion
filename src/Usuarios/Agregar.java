    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int Acceso=0;
        switch (jComboBox1.getSelectedItem().toString()){
            case "Administrador": 
                Acceso = 999;
                break;
            case "Fusion":
                Acceso = 200;
                break;
            case "Cerrado":
                Acceso = 100;
                break;       
        }
        if(!TxtNombre.getText().equals("") && !TxtContrasena.getText().equals("") && !TxtUsuario.getText().equals("")){
          fusion.Conexion.AgregaUsuario(TxtNombre.getText(),TxtUsuario.getText(),TxtContrasena.getText(),Acceso);   
        }else {
            JOptionPane.showMessageDialog(this, "Los campos no pueden quedar en blanco.");
        }       
    }//GEN-LAST:event_jButton1ActionPerformed

