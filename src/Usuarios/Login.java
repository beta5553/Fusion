    private void BtnAccesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAccesoActionPerformed
        // TODO add your handling code here:
        if (!TxtUser.getText().contains("'") && !TxtPass.getText().contains("'")) {
            if (!TxtUser.getText().equals("") && !TxtPass.getText().equals("")) {
                if (Cn.QueryLogin(TxtUser.getText(), TxtPass.getText())) {
                    this.hide();
                    Calendar calendar = Calendar.getInstance();
                    int NumSem = calendar.get(Calendar.WEEK_OF_YEAR);
                    int NumAnio = calendar.get(Calendar.YEAR);
                    if(fusion.IMF.LblCurrentAccess.equals("999")){
                  Cn.CargarInventario(NumSem, NumAnio);       
                    }                   
                } else {
                    JOptionPane.showMessageDialog(this, "Usuario invalido");
                }
            } else {
                IMF Principal = new IMF();
                Principal.setBounds(0, 0, 1440, 900);
                Principal.show();
                fusion.IMF.CurrentUser.setText("view");
                fusion.IMF.LblCurrentAccess.setText("0");
                this.hide();
                JOptionPane.showMessageDialog(this, "Entro al Modo View");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Usuario invalido");
        }

    }//GEN-LAST:event_BtnAccesoActionPerformed


